import java.util.Scanner;


public class Multiply { // Multiply라는 이름을 가진 클래스

	public static void main(String[] args) { //메인 메소드 -> 프로그램 시작되는 지점
		int a = 10; //a라는 변수선언이후에 10을 넣어줌
		int b = 20; //b라는 변수선언이후에 20을 넣어줌
		int c = a*b; //c라는 변수선언이후에 a*b의 곱을 c에 넣어줌
		
		System.out.println("a와 b의 곱"+c+"입니다"); // ""안의 내용(a와b의 곱은200입니다)을 콘솔에 입력 (+를 입력하면 같이 연결해서 출력 가능)
	}

}


