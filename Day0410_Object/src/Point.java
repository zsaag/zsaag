
public class Point {
	private int x;
	private int y;
	public void setX(int x) {
		this.x = x;
	}
	public int getX() {
		return x;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getY() {
		return y;
	}
	//2차원 좌표값을 저장하는 클래스
	//x(정수),y(정수)
	//getter//setter
	//toString 만들어주세요

	public String toString() {
		return "Point [x=" + x + ", y=" + y + "]";
	}
	
}
